// = assignment operator
// == comparison operator checks the value and not the type
// === strict equality operator

let x = "3.14"; // use to assign the value to a variable
// == use is to just check the value and not the data type
if(x == 3.14){
    console.log("That is pi");
}
else{
    console.log("That is NOT pi");
}
//=== use is to check both datatype and the value
if(x === 3.14){
    console.log("That is pi");
}
else{
    console.log("That is NOT pi");
}